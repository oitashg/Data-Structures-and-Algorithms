// 1's complement
// Convert the number into binary and flip all the bits
// Eg:- 5 -> 101. So, 1's complement of 5 is 010

// 2's complement
// Evaluate 1's complement and perform binary addition(OR) of 1(0000..1) to it 
// Eg:- 5 -> 101. So, 1's complement of 5 is 010
// So, 2's complemet of 5 is (010 + 001) = 011
// In 2's complement only the last bit changes if that bit was 0 in 1's complement because 0 OR 1 is 1